    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String searchValue = txtSearch.getText().toLowerCase().trim();
        if (searchValue.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Nhập thông tin cần tìm kiếm", "Error",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String searchOption = (String) cbSearch.getSelectedItem();
        ArrayList<Printer> searchList = new ArrayList<>();
        if (searchOption.equals("Tìm kiếm theo tên")) {
            for (Printer p : list) {
                if (p.getName().toLowerCase().equals(searchValue))
                    searchList.add(p);
            }
        }
        if (searchOption.equals("Tìm kiếm theo hãng")) {
            for (Printer p : list) {
                if (p.getManufacturer().toLowerCase().equals(searchValue))
                    searchList.add(p);
            }
        }
        if (searchOption.equals("Tìm kiếm theo loại máy in")) {
            for (Printer p : list) {
                if (p.getType().toLowerCase().equals(searchValue))
                    searchList.add(p);
            }
        }
        if (searchOption.equals("Tìm kiếm theo năm")) {
            for (Printer p : list) {
                if (p.getYear() == Integer.parseInt(searchValue))
                    searchList.add(p);
            }
        }
        displayData(searchList);
        emptyField();
    }//GEN-LAST:event_btnSearchActionPerformed

